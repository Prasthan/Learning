// if-else condition
// for more use "else if"
let myAge = 17;

if(myAge > 18){
    document.write("You are eligible.")
}else{
    document.write("You are not eligible.")
}



// for loop
for(let i = 1; i<=100; i++){
    document.write("<br>"+i);       // gives 1 to 100 numbers
    document.write("Prasthan <br>");         // gives Prasthan 100 times 
}



// while loop
let i = 0;
while(i<=100){
    document.write(i);
    i++;
}